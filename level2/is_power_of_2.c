#include <stdio.h>

int    is_power_of_2(unsigned int n)
{
    while (n % 2 == 0)
    {
        n = n / 2;
    }
    if (n == 1)
        return (1);
    return (0);
}

int main()
{
    int a;
    a = is_power_of_2(2048);
    printf("%d", a);
    return (0);
}